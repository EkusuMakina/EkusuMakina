/*企业发放的奖金根据利润提成。利润(I)低于或等于10万元时，
奖金可提10%；利润高于10万元，低于20万元时，低于10万元的部分按10%提成，
高于10万元的部分，可提成7.5%；20万到40万之间时，高于20万元的部分，可提成5%；
40万到60万之间时高于40万元的部分，可提成3%；60万到100万之间时，高于60万元的部分，可提成1.5%，
高于100万元时，超过100万元的部分按1%提成，从键盘输入当月利润I，求应发放奖金总数？*/
#include<iostream>
#include<stdio.h>
using namespace std;
int main()
{
    int a;
    float t;
    cin>>a;
    if(a<=10)
    {
        t=a*0.1;
    }
    if(a>10&&a<=20)
    {
        t=10*0.1+(a-10)*0.075;
    }
    if(a>20&&a<=40)
    {
        t=10*0.1+10*0.075+(a-20)*0.05;
    }
    if(a>40&&a<=60)
    {
        t=10*0.1+10*0.075+20*0.05+(a-40)*0.03;
    }
    if(a>60&&a<=100)
    {
        t=10*0.1+10*0.075+20*0.05+20*0.03+(a-60)*0.015;
    }
    if(a>100)
    {
        t=10*0.1+10*0.075+20*0.05+20*0.03+40*0.015+(a-100)*0.01;
    }
    printf("%.6f",t);
}
